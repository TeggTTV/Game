function loadImages() {
    // open file explorer so user can select image
    let input = document.createElement("input");
    input.type = "file";
    input.accept = "image/*";
    input.click();
    input.onchange = (e) => {
        let file = e.target.files[0];
        let reader = new FileReader();
        reader.readAsDataURL(file);
        reader.onload = (e) => {
            let img = new Image();
            img.src = e.target.result;
            img.onload = () => {
                const size = prompt(
                    "Enter the size of the tilesheet (e.g. 64)"
                );
                const tileSize = parseInt(size);
                const rows = img.width / tileSize;
                const cols = img.height / tileSize;
                loadTileSheet(e.target.result, width, height, rows, cols).then(
                    (tiles) => {
                        console.log(tiles);
                    }
                );
            };
        };
    };
}

